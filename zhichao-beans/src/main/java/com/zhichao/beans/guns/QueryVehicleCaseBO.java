package com.zhichao.beans.guns;

import java.io.Serializable;
import java.util.Date;

/**
 * 接收 查询车辆违章案件的BO类
 * @author imyzt
 *
 */
public class QueryVehicleCaseBO implements Serializable{

	private static final long serialVersionUID = 1L;
	
	/**
	 * 违章表的id
	 */
	private Integer id;
	
	/**
	 * 检测单号
	 */
	private String checkno;
	
	/**
	 * 检测时间
	 */
	private Date ctime;
	
	/**
	 * 检测站点
	 */
	private String stationid;
	
	/**
	 * 车辆总重KG
	 */
	private Integer weightlimited;
	
	/**
	 * 轴数
	 */
	private Integer axlesum;
	
	/**
	 * 超限率
	 */
	private Double rate;
	
	/**
	 * 超限数KG
	 */
	private Integer overlimited;
	
	/**
	 * 状态
	 */
	private String prostatus;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getCheckno() {
		return checkno;
	}

	public void setCheckno(String checkno) {
		this.checkno = checkno;
	}

	public Date getCtime() {
		return ctime;
	}

	public void setCtime(Date ctime) {
		this.ctime = ctime;
	}

	public String getStationid() {
		return stationid;
	}

	public void setStationid(String stationid) {
		this.stationid = stationid;
	}

	public Integer getWeightlimited() {
		return weightlimited;
	}

	public void setWeightlimited(Integer weightlimited) {
		this.weightlimited = weightlimited;
	}

	public Integer getAxlesum() {
		return axlesum;
	}

	public void setAxlesum(Integer axlesum) {
		this.axlesum = axlesum;
	}

	public Double getRate() {
		return rate;
	}

	public void setRate(Double rate) {
		this.rate = rate;
	}

	public Integer getOverlimited() {
		return overlimited;
	}

	public void setOverlimited(Integer overlimited) {
		this.overlimited = overlimited;
	}

	public String getProstatus() {
		return prostatus;
	}

	public void setProstatus(String prostatus) {
		this.prostatus = prostatus;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	@Override
	public String toString() {
		return "QueryVehicleCaseBO [id=" + id + ", checkno=" + checkno + ", ctime=" + ctime + ", stationid=" + stationid
				+ ", weightlimited=" + weightlimited + ", axlesum=" + axlesum + ", rate=" + rate + ", overlimited="
				+ overlimited + ", prostatus=" + prostatus + "]";
	}
}
